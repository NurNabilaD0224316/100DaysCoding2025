public class Day10 {
    public static void main(String[] args) {
        // Variabel biasa
        int umur = 19;
        System.out.println("Umur awal: " + umur);

        // nilainya bisa diubah
        umur = 20; 
        System.out.println("Umur setelah diubah: " + umur);

        // Variabel final (konstanta)
        final double PI = 3.14159;
        System.out.println("Nilai PI: " + PI);

       
    }
}
