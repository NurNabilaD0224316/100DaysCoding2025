public class Day15{
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Penjumlahan
        int hasilTambah = a + b;

        // Pengurangan
        int hasilKurang = a - b;

        // Output
        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Hasil Penjumlahan: " + hasilTambah);
        System.out.println("Hasil Pengurangan: " + hasilKurang);
    }
}

	
