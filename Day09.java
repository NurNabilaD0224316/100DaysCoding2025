public class Day09 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int umur;
        String nama;

        // Inisialisasi variabel (memberi nilai awal)
        umur = 19;
        nama = "Nabila";

        // Cetak nilai awal
        System.out.println("Nama : " + nama);
        System.out.println("Umur : " + umur);

        // Update nilai variabel
        umur = 20;  
        nama = "Nur Nabila";  

        // Cetak nilai setelah diupdate
        System.out.println("\nSetelah update:");
        System.out.println("Nama : " + nama);
        System.out.println("Umur : " + umur);
    
	}
}
