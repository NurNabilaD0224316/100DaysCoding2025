public class Day16{
    public static void main(String[] args) {
        int a = 20;
        int b = 4;

        // Perkalian
        int hasilKali = a * b;

        // Pembagian
        int hasilBagi = a / b;

        // Output
        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Hasil Perkalian = " + hasilKali);
        System.out.println("Hasil Pembagian = " + hasilBagi);
    }
}
